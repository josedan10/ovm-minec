    public function changeActiveState(Request $request, $id)
    {
        if ($request->validate([
            'is_active' => 'boolean'
        ])) {

            try {
                $post = Post::find($id);
                $post->is_active = $request->input('is_active');
                $post->save();
                return response('Post actualizado', 200);
            } catch (Exception $err) {

                return response($err->getMessage(), 500);
            }
        }
    }
